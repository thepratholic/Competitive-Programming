void solve() {
    int n, x;
    cin >> n >> x;
    int o = 0, e = 0;
    for (int i = 0; i < n; i++) {
        int v;
        cin >> v;
        if (v & 1) o++;
        else       e++;
    }

    int take_odd = min(o, x);
    if (take_odd % 2 == 0) take_odd--; 
    if (take_odd >= 1 && x - take_odd <= e) 
        cout << "Yes\n";
    else
        cout << "No\n";
}

