ll CHOICE(int n, int k) {
    auto modPow = [](ll base, ll exp, ll mod) {
        ll result = 1;
        while (exp > 0) {
            if (exp % 2 == 1) {
                result = (result * base) % mod;
            }
            base = (base * base) % mod;
            exp /= 2;
        }
        return result;
    };
    auto factorial = [](ll num, ll mod) {
        ll result = 1;
        for (ll i = 2; i <= num; i++) {
            result = (result * i) % mod;
        }
        return result;
    };
    if (k < 0 || k > n) {
        return 0;
    }
    ll numerator = factorial(n, MOD);
    ll denominator = (factorial(k, MOD) * factorial(n - k, MOD)) % MOD;
    return (numerator * modPow(denominator, MOD - 2, MOD)) % MOD;
}

