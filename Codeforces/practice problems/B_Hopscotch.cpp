string ADDBIN(string a, string b){
  string result = "";
  ll temp = 0;
  ll size_a = a.size() - 1;
  ll size_b = b.size() - 1;
  while (size_a >= 0 || size_b >= 0 || temp == 1) {
    temp += ((size_a >= 0) ? a[size_a] - '0' : 0);
    temp += ((size_b >= 0) ? b[size_b] - '0' : 0);
    result = char(temp % 2 + '0') + result;
    temp /= 2;
    size_a--;
    size_b--;
  }
  return result;
}


ld EVAL(string s){
  string ma;
  FORO(i,s){
    if(s[i]!=' '){
      ma+=s[i];
    }
  }
  V adad;
  vector<char> amal;
  FORO(i,ma){
    if(ma[i]=='('){
      ld iter=0;
      iter++;
      string e="";
      i++;
      while(true){
        if(ma[i]==')'){
          iter--;
          if(iter==0){
            adad.pb(EVAL(e));
            break;
          }
        }
        if(ma[i]=='(')iter++;
        e+=ma[i];
        i++;
      }
    }
    else if(ISDIGIT(ma[i])){
      string r="";
      r+=ma[i];
      while(ISDIGIT(ma[i+1])){
        r+=ma[i+1];
        i++;
      }
      adad.pb(stoi(r));
    }
    else{
      amal.pb(ma[i]);
    }
  }
  FORO(i,amal){
    if(amal[i]=='/'){
      if(adad[i+1]==0){
        return ninf;
      }
      adad[i]=adad[i]/adad[i+1];
      adad.erase(adad.begin()+i+1);
      amal.erase(amal.begin()+i);
      i--;
    }
    else if(amal[i]=='*'){
      adad[i]=adad[i]*adad[i+1];
      adad.erase(adad.begin()+i+1);
      amal.erase(amal.begin()+i);
      i--;
    }
  }
  if(adad.size()==1){
    return adad[0];
  }
  ld javab=adad[0];
  FORO(i,amal){
    if(amal[i]=='+'){
      javab+=adad[i+1];
    }
    else{
      javab-=adad[i+1];
    }
  }

  return javab;
}

